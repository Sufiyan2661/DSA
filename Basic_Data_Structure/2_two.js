// Access an Array contents using bracket notation

let ourArray = ["a","b","c"]
console.log(ourArray)

// In order to access an array element and store it to a variable we use bracket notation.

let ouVariable = ourArray[0]
console.log(ouVariable)
// Now the ouVariable has element a  init 

// We can also set an index to a value using the same notation.

ourArray[1] = "not b anymore"

console.log(ourArray)

